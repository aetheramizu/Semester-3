# Macam-macam tipe data di Python

# Integer (bilangan bulat)
angka_bulat = 10
print("Integer:", angka_bulat, type(angka_bulat))

# Float (bilangan desimal)
angka_desimal = 3.14
print("Float:", angka_desimal, type(angka_desimal))

# String (teks)
teks = "Halo, Python!"
print("String:", teks, type(teks))

# Boolean (true/false)
status = True
print("Boolean:", status, type(status))

# List (kumpulan data yang bisa diubah)
daftar = [1, 2, 3, "empat", 5.0]
print("List:", daftar, type(daftar))

# Tuple (kumpulan data tetap, tidak bisa diubah)
data_tetap = (1, 2, 3)
print("Tuple:", data_tetap, type(data_tetap))

# Set (kumpulan data unik, tanpa urutan)
data_unik = {1, 2, 2, 3, 4}
print("Set:", data_unik, type(data_unik))

# Dictionary (pasangan key-value)
mahasiswa = {"nama": "Alief", "umur": 18, "jurusan": "Teknik Informatika"}
print("Dictionary:", mahasiswa, type(mahasiswa))
